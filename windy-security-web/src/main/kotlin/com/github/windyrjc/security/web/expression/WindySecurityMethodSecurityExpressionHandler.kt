class WindySecurityMethodSecurityExpressionHandler: DefaultMethodSecurityExpressionHandler(){


    override fun createSecurityExpressionRoot(authentication: Authentication, invocation: MethodInvocation): MethodSecurityExpressionOperations {

        val root = WindySecurityExpressionRoot(authentication)
        root.target = invocation.`this`
        root.permissionEvaluator = permissionEvaluator
        root.trustResolver = trustResolver
        root.roleHierarchy = roleHierarchy
        root.rolePrefix = defaultRolePrefix
        return root
    }
}