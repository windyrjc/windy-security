class WindySecurityAuthenticationEntryPoint(val objectMapper: ObjectMapper) : AuthenticationEntryPoint {

    val log = LoggerFactory.getLogger(WindySecurityAuthenticationEntryPoint::class.java)

    override fun commence(request: HttpServletRequest, response: HttpServletResponse, exception: AuthenticationException) {
        log.info(request.requestURI + ":" + exception.message)
        response.characterEncoding = "UTF-8"
        response.contentType = MediaType.APPLICATION_JSON_UTF8_VALUE
        response.status = HttpStatus.UNAUTHORIZED.value()
        response.writer.write(objectMapper.writeValueAsString(Response.fail(msg = exception.message!!)))
    }

}